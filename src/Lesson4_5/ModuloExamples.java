package Lesson4_5;

public class ModuloExamples {
        public static void main(String[] args) {
            // Деление на 2
            int number1 = 10;
            int remainder1 = number1 % 2;
            System.out.println("Остаток от деления " + number1 + " на 2: " + remainder1);

            int number2 = 15;
            int remainder2 = number2 % 2;
            System.out.println("Остаток от деления " + number2 + " на 2: " + remainder2);

            // Деление на 3
            int number3 = 11;
            int remainder3 = number3 % 3;
            System.out.println("Остаток от деления " + number3 + " на 3: " + remainder3);

            int number4 = 20;
            int remainder4 = number4 % 3;
            System.out.println("Остаток от деления " + number4 + " на 3: " + remainder4);

        }
}
